
public class MathTest {

    public static void main(String[] args) {
        Double a = Math.sqrt(37.0);
        Double b = Math.sin(300.0);
        Double b1 = Math.cos(300.0);
        Long c = Math.round(22.8);
        Double c2 = Math.floor(22.8);
        double d = Math.random();
        System.out.println("La raiz cuadrada de 37 es: "+a);
        System.out.println("El seno de 300 es: "+b);
        System.out.println("El cose de 300 es: "+b1);
        System.out.println("22.8 redondeado es: "+c);
        System.out.println("22.8 redondeado es: "+c2);
        System.out.println("Numero aleatorio: "+d);
    }
}
